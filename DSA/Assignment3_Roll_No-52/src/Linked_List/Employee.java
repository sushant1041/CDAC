package Linked_List;

class Employee {
    int employeeId;
    String employeeName;
    String gender;
    int salary;

    public Employee(int employeeId, String employeeName, String gender, int salary) {
        this.employeeId = employeeId;
        this.employeeName = employeeName;
        this.gender = gender;
        this.salary = salary;
    }
}
